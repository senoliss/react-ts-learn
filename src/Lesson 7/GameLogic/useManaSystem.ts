import { useState } from 'react';

export function useManaSystem(maxMana: number = 100) {
    const [mana, setMana] = useState<number>(maxMana);

    const castSpell = (cost: number) =>
        {
            if (cost > mana) {
            console.warn("Not enough mana!, cost:", cost, "current mana:", mana);
            return;
        }
        setMana(m => Math.max(0, m - cost));
    };

    const regenMana = (amount: number) => {
        console.log(`Mana regenerated by ${amount}`);
        setMana(m => Math.min(maxMana, m + amount));
    }
    

    return {
        mana,
        castSpell,
        regenMana,
        setMana
    };
}